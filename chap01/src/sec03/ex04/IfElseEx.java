package sec03.ex04;

public class IfElseEx {

	public static void main(String[] args) {
		//if else문
		int score = 85;
		
		if(score>=90) {
			System.out.println("점수가 90보다 큽니다.");
			System.out.println("A등급입니다.");
		}else {
			System.out.println("점수가 90점 보다 작습니다.");
			System.out.println("B등급입니다.");
		}

	}

}
