package sec03.ex04;

public class IfElseElseIfEx {

	public static void main(String[] args) {
		// if-else else-if 문 예제
		int score = 75;
		if(score>=90) {
			System.out.println("A");
		}else if(score>=80) {
			System.out.println("B");
		}else if(score>=70) {
			System.out.println("C");
		}else {
			System.out.println("D");
		}

	}

}
